### 4.集合（Set）####
###################
# 集合是一种无序的、可变的数据容器，可以存储多个唯一的元素。
# 定义：
# 集合名 = {元素1, 元素2, 元素3, ...}
# *******定义空集合*******
s = set()  # 不可以使用 : s = {}
s = {}  # 这种写法定义的是一个***空字典***
# ==>元素可以是任意类型的数据，包括数字、字符串、布尔值、甚至其他集合等。
# ==>元素是无序的，不能通过索引来访问。
# ==>是可变的，可以添加、删除元素，但不能修改元素。
# ==>不允许重复的元素，如果添加重复的元素，集合会自动去重。
# 集合支持多种内置方法，如add()、remove()、pop()、union()、intersection()等，可以方便地进行各种操作。
# add()         :添加元素
# remove()      :删除指定元素
# pop()         :***随机***删除元素并作为返回值
# clear()       :清空
# difference()  :求两个集合的差集<==>集合1 中 不属于集合2 的元素
# union()       :求集合并集<==>集合1 ∪ 集合2
# intersection():求集合交集<==>集合1 ∩ 集和2
#
# 集合适用于存储不需要重复的元素集合，或者进行集合运算等场景。
str3 = "Apoptoxin-4869"
str4 = "APTX-4869"

u1 = set()
u2 = set()

for i in str3:
    u1.add(i)
for i in str4:
    u2.add(i)

print(f"u1={u1},\nu2={u2}")
print(f"u1.difference(u2)={u1.difference(u2)}")
print(f"u1.union(u2)={u1.union(u2)}")
print(f"u1.intersection(u2)={u1.intersection(u2)}")
print()

