#### 1.列表（List）###
#####################
# 一次性可以存储多个数据（元素）的。
# 定义：
# 列表名 = [元素1, 元素2, 元素3, ...]
# 列表中的元素可以是任意类型的数据，包括数字、字符串、布尔值、甚至其他列表等。
# 列表中的元素是有序的，可以通过索引来访问和修改。
# s[索引数字]：数字大于0，正向索引；数字小于0，反向索引；
# 列表是可变的，可以添加、删除、修改元素。
# 列元素可以重复
s = [12, 24, "helllo", '!', True]
s2 = [12, s, 12]
print(s2[1][2], s2[-2][3], sep="")
print()
# 正向索引从‘0’开始，反向索引从‘-1’开始
# 删除
print(s)
print("使用del(s[索引])删除元素")
del s[4]
print(s)
print()

# 列表支持切片操作，可以获取列表的子列表。
# 切片语法：列表名[起始索引:结束索引:步长]
# 起始索引默认为0，结束索引默认为列表的长度，步长默认为1
print(s)
print("使用切片操作获取列表的子列表")
print(s[1:4])  # 获取索引1到3的元素，左闭右开区间，能取到1但不能取到4
print(s[:3])  # 获取索引0到2的元素，左闭右开区间，能取到0但不能取到3
print(s[2:])  # 获取索引2到最后的元素
print(s[::2])  # 获取索引0到最后的元素，步长为2，能取到0、2、4等索引的元素
print("列表切片的类型为", type(s[1::2]))

# list的方法：
# 列表支持多种内置方法，如append()、extend()、insert()、remove()、pop()、sort()等，可以方便地进行各种操作。
# 调用方法和cpp类似
# append():在list尾部追加元素
# insert():在指定索引之前插入元素,如果索引超过列表长度，则在列表尾部插入元素
# remove():删除列表中第一匹配的元素
# pop():删除列表中指定索引的元素，并返回该元素的值，如果不指定索引，则删除列表中的最后一个元素
# sort():列表数据一致时，按照升序排序；
# sort()方法会修改原列表，如果不想修改原列表，可以使用sorted()函数
# reverse():逆置列表中的元素
# 逆置前
print("逆置之前:")
print(s)
s.reverse()
print("逆置之后:")
print(s)
print()

# ******列表推导式******
# 格式: [要插入的值 for i in 序列/列表 if 附加条件]
num_square = [i**2 for i in range(1, 20) if i % 2 == 0]
print(num_square)
print()

