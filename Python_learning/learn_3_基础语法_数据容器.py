##2.字符串(String)###
#####################
# 也是一种数据容器，可以存储多个字符的序列。
# 定义：字符串名 = "字符序列"
# 字符串中的字符可以是任意类型的数据，包括数字、字母、符号等。
# 字符串中的字符是有序的，可以通过索引来访问。
# 字符串是不可变的，一旦定义后就不能修改字符。
# 字符串支持切片操作，可以获取字符串的子字符串。
# 切片语法：字符串名[起始索引:结束索引:步长]
# 起始索引默认为0，结束索引默认为字符串的长度，步长默认为1
# 字符串支持多种内置方法，如upper()、lower()、strip()、split()等，可以方便地进行各种操作。
# 字符串适用于存储和处理文本数据，或者作为函数的参数等场景。

