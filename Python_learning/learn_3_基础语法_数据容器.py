## 3.元组（Tuple）####
#####################
# 元组也是一种数据容器，可以存储多个数据（元素）的。
# 定义：
# 元组名 = (元素1, 元素2, 元素3, ...)
# 元组中的元素可以是任意类型的数据，包括数字、字符串、布尔值、甚至其他元组等。
# 元组中的元素是有序的，可以通过索引来访问。
# 元组是不可变的，一旦定义后就不能修改元素。
# 元组支持切片操作，可以获取元组的子元组。
# 元组支持多种内置方法，如count()、index()等，可以方便地进行各种操作。
# 元组适用于存储不需要修改的数据集合，或者作为函数的返回值等场景。


# 4.集合（Set）
# 集合是一种无序的、可变的数据容器，可以存储多个唯一的元素。
# 定义：
# 集合名 = {元素1, 元素2, 元素3, ...}
# 集合中的元素可以是任意类型的数据，包括数字、字符串、布尔值、甚至其他集合等。
# 集合中的元素是无序的，不能通过索引来访问。
# 集合是可变的，可以添加、删除元素，但不能修改元素。
# 集合不允许重复的元素，如果添加重复的元素，集合会自动去重。
# 集合支持多种内置方法，如add()、remove()、pop()、union()、intersection()等，可以方便地进行各种操作。
# 集合适用于存储不需要重复的元素集合，或者进行集合运算等场景。


# 5.字典（Dictionary）
# 字典是一种无序的、可变的数据容器，可以存储多个键值对（key-value pairs）。
# 定义：
# 字典名 = {键1: 值1, 键2: 值2, 键3: 值3, ...}
# 字典中的键必须是唯一的，可以是任意不可变类型的数据，如字符串、数字、元组等。
# 字典中的值可以是任意类型的数据，包括数字、字符串、布尔值、甚至其他字典等。
# 字典中的键值对是无序的，不能通过索引来访问，但可以通过键来访问对应的值。
# 字典是可变的，可以添加、删除、修改键值对。
# 字典支持多种内置方法，如keys()、values()、items()、get()、update()等，可以方便地进行各种操作。
# 字典适用于存储需要通过键来访问的元素集合，或者进行键值对操作等场景。


# 6.命名元组（NamedTuple）
# 命名元组是一种特殊的元组，可以通过名称来访问元素，而不是通过索引。
# 定义：
# from collections import namedtuple
# 命名元组名 = namedtuple('命名元组名', ['字段1', '字段2', '字段3', ...])
# 命名元组中的字段可以是任意类型的数据，包括数字、字符串、布尔值、甚至其他命名元组等。
# 命名元组中的字段是有序的，可以通过索引来访问，也可以通过名称来访问。
# 命名元组是不可变的，一旦定义后就不能修改字段。
# 命名元组支持多种内置方法，如_._fields()、_._asdict()等，可以方便地进行各种操作。
# 命名元组适用于存储需要通过名称来访问的元素集合，或者作为函数的返回值等场景。


# 7.数据类（Data Class）
# 数据类是一种特殊的类，用于存储数据，并自动生成一些常用的方法，如__init__()、__repr__()、__eq__()等。
# 定义：
# from dataclasses import dataclass
# @dataclass
# class 数据类名:
#     字段1: 类型1
#     字段2: 类型2
#     字段3: 类型3
#     ...
# 数据类中的字段可以是任意类型的数据，包括数字、字符串、布尔值、甚至其他数据类等。
# 数据类中的字段是有序的，可以通过索引来访问，也可以通过名称来访问。
# 数据类是可变的，可以修改字段的值。
# 数据类支持多种内置方法，如__init__()、__repr__()、__eq__()等，可以方便地进行各种操作。
# 数据类适用于存储需要通过名称来访问的元素集合，或者作为函数的返回值等场景，特别适用于需要存储大量数据的场景，可以减少代码的冗余和提高代码的可读性。
