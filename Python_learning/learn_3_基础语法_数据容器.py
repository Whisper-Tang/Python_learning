# 7.数据类（Data Class）
# 数据类是一种特殊的类，用于存储数据，并自动生成一些常用的方法，如__init__()、__repr__()、__eq__()等。
# 定义：
# from dataclasses import dataclass
# @dataclass
# class 数据类名:
#     字段1: 类型1
#     字段2: 类型2
#     字段3: 类型3
#     ...
# 数据类中的字段可以是任意类型的数据，包括数字、字符串、布尔值、甚至其他数据类等。
# 数据类中的字段是有序的，可以通过索引来访问，也可以通过名称来访问。
# 数据类是可变的，可以修改字段的值。
# 数据类支持多种内置方法，如__init__()、__repr__()、__eq__()等，可以方便地进行各种操作。
# 数据类适用于存储需要通过名称来访问的元素集合，或者作为函数的返回值等场景，特别适用于需要存储大量数据的场景，可以减少代码的冗余和提高代码的可读性。
